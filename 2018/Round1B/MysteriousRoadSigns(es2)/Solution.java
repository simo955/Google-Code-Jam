	private static Scanner sc;
	private static Printer pr;

	private static void solve() {
		int t = sc.nextInt();

		for (int casei = 1; casei <= t; casei++) {
			int s = sc.nextInt();

			int[] d = new int[s];
			int[] a = new int[s];
			int[] b = new int[s];
			int[] n = new int[s];
			int[] m = new int[s];

			Set<Integer> hsn = new HashSet<>();
			Set<Integer> hsm = new HashSet<>();
			for (int i = 0; i < s; i++) {
				d[i] = sc.nextInt();
				a[i] = sc.nextInt();
				b[i] = sc.nextInt();

				n[i] = d[i] + a[i];
				m[i] = d[i] - b[i];
				hsn.add(n[i]);
				hsm.add(m[i]);
			}

			int max = 1;
			Set<Pair> cnt = new HashSet<>();
			for (int nn : hsn) {
				for (int mm : hsm) {
					int prev = -1;
					int tmp = 0;
					for (int i = 0; i < s; i++) {
						if (n[i] == nn || m[i] == mm) {
							if (prev == -1) {
								prev = i;
								tmp = 1;
							} else {
								tmp = i - prev + 1;
							}
						} else {
							prev = -1;
							tmp = 0;
						}

						if (tmp > max) {
							max = tmp;
							cnt = new HashSet<>();
							cnt.add(new Pair(prev, i));
						} else if (tmp == max) {
							cnt.add(new Pair(prev, i));
						}
					}
				}
			}

			pr.printf("Case #%d: %d %d\n", casei, max, cnt.size());
		}
	}

	static class Pair implements Comparable<Pair> {
		int a;
		int b;

		Pair(int a, int b) {
			this.a = a;
			this.b = b;
		}

		@Override
		public int compareTo(Pair o) {
			if (a == o.a) {
				return Integer.compare(b, o.b);
			}

			return Integer.compare(a, o.a);
		}

		@Override
		public int hashCode() {
			return Integer.hashCode(a + b);
		}

		@Override
		public boolean equals(Object obj) {
			Pair o = (Pair)obj;
			return a == o.a && b == o.b;
		}

		@Override
		public String toString() {
			// [xxx, xxxx]
			StringBuilder stmp = new StringBuilder(32);
			stmp.append('[');
			stmp.append(a);
			stmp.append(',');
			stmp.append(' ');
			stmp.append(b);
			stmp.append(']');

			return stmp.toString();
		}
	}

